void main(List<String> args) {
  //numeric data types
  int a = 10; //int is used to stroe integer values(no fractional part)
  double b = 10.5; //double is used to store decimal values
  num c = 11; //num is used to store both int and double values
  num d = 56.37; //num is used to store both int and double values
  var e = 12; //var is used to store any data type
  var f = 12.5; //var is used to store any data type
  print("These are the values of a,b,c,d,e,f");
  print(a); //10
  print(b); //10.5
  print(c); //11
  print(d); //56.37
  print(e); //12
  print(f); //12.5
  //string data types
  String g = "Hello"; //String is used to store string values
  var h = "World"; //var is used to store any data type
  print("These are the values of g,h");
  print(g); //Hello
  print(h); //World
}
